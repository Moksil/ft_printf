#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (lst == NULL)
		return (NULL);
	while (lst)
	{
		if (lst -> next)
			lst = lst -> next;
		else
			break ;
	}
	return (lst);
}
