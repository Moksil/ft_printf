#include "ft_printf.h"

void	put_nbr(int n)
{
	if (n == -2147483648)
		write(1, "2147483648", 10);
	else if (n < 0)
	{
		write(1, "-", 1);
		put_nbr((-1) * n);
	}
	else if (n < 10)
		ft_putchar_fd('0' + n, 1);
	else
	{
		put_nbr(n / 10);
		ft_putchar_fd('0' + (n % 10), 1);
	}
}
